  names(parms) <- lapply(names(parms), function(i) { if( i %in% names(.quantile.map) ) i <- .quantile.map[[i]]; i })

  model <- .h2o.createModel(x@conn, "quantile", parms, parent.frame())

  col <- model@model$quantile[[1L]]
  names(col) <- paste0(100*probs, "%")
  col
